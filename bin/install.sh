---

## ✅ 检查清单

执行前：
- [ ] 确认任务目标
- [ ] 确认交付形式

执行后：
- [ ] 结果符合要求
- [ ] 记录经验教训

---

*使用智能任务管理器创建和管理流程文档*
EOF
    echo -e "${GREEN}✅ 流程文档模板已创建${NC}"
fi

# 完成
echo ""
echo "================================"
echo -e "${GREEN}✅ 安装完成！${NC}"
echo ""
echo "📋 下一步："
echo "   1. 重启网关：openclaw gateway restart"
echo "   2. 开始新对话：/new"
echo "   3. 测试 Skill：你好"
echo ""
echo "📚 文档位置："
echo "   - ~/.openclaw/workspace/任务类型映射表.md"
echo "   - ~/.openclaw/workspace/快速通道任务列表.md"
echo "   - ~/.openclaw/workspace/通用任务执行流程.md"
echo "   - ~/.openclaw/workspace/流程文档模板.md"
echo ""
echo "✨ 享受智能任务管理吧！"
echo "================================"
