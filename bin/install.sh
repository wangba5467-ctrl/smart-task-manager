#!/bin/bash

# 智能任务管理器 - 安装脚本
# 适用于 Linux/macOS

set -e

echo "🧠 智能任务管理器 - 安装脚本"
echo "================================"

# 颜色定义
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# 获取 OpenClaw 目录
OPENCLAW_DIR="${OPENCLAW_DIR:-$HOME/.openclaw}"
WORKSPACE_DIR="$OPENCLAW_DIR/workspace"

echo ""
echo "📁 OpenClaw 目录: $OPENCLAW_DIR"
echo "📁 工作区目录: $WORKSPACE_DIR"

# 检查目录
if [ ! -d "$OPENCLAW_DIR" ]; then
    echo "❌ 错误：OpenClaw 目录不存在"
    echo "   请先安装 OpenClaw"
    exit 1
fi

# 创建工作区目录
if [ ! -d "$WORKSPACE_DIR" ]; then
    echo ""
    echo "📦 创建工作区目录..."
    mkdir -p "$WORKSPACE_DIR"
    echo -e "${GREEN}✅ 工作区目录已创建${NC}"
fi

# 复制配置文件
echo ""
echo "📦 复制配置文件..."

# 检查配置文件是否已存在
if [ -f "$WORKSPACE_DIR/任务类型映射表.md" ]; then
    echo -e "${YELLOW}⚠️  任务类型映射表已存在，跳过${NC}"
else
    cat > "$WORKSPACE_DIR/任务类型映射表.md" << 'EOF'
# 任务类型映射表

> **⚠️ 每次执行任务前必须查阅此表**

---

## ⚡ 快速通道（优先检查）

**快速通道包括：**
- 信息查询类（查文件、查信息、查文档、查历史、查命令）
- 计算类（简单计算、单位转换、时间计算、数据统计）
- 生成类（小段代码、短文本、简单配置、格式化）
- 辅助类（解释说明、翻译、总结、校对）
- 娱乐类（说笑话、闲聊、测试）

---

## 📋 任务类型对照

| 任务类型 | 触发词 | 流程文档 | 状态 |
|---------|--------|---------|:----:|
| **公众号写作** | 写文章、改写、润色、公众号、选题 | （待创建）| ❌ |
| **配图** | 配图、生成图片、封面图 | （待创建）| ❌ |
| **代码开发** | 写代码、开发、调试、编程 | （待创建）| ❌ |
| **数据分析** | 分析、统计、报表、数据 | （待创建）| ❌ |
| **文档整理** | 整理文档、总结、归纳 | （待创建）| ❌ |
| **服务器管理** | 服务器、部署、运维 | （待创建）| ❌ |
| **其他任务** | （未匹配的任务）| 通用任务执行流程.md | ✅ |

---

*使用智能任务管理器自动创建和管理流程文档*
EOF
    echo -e "${GREEN}✅ 任务类型映射表已创建${NC}"
fi

if [ -f "$WORKSPACE_DIR/快速通道任务列表.md" ]; then
    echo -e "${YELLOW}⚠️  快速通道任务列表已存在，跳过${NC}"
else
    cat > "$WORKSPACE_DIR/快速通道任务列表.md" << 'EOF'
# 快速通道任务列表

> **⚡ 以下任务可以直接执行，不需要走完整流程**

---

## 🎯 快速通道任务

### 信息查询类（秒级任务）

| 任务 | 说明 | 示例 |
|------|------|------|
| **查文件** | 查找本地文件 | "帮我找 xxx 文件" |
| **查信息** | 查询已知信息 | "Python 的版本是什么？" |
| **查文档** | 读取文档内容 | "看一下 xxx 文档" |
| **查历史** | 查看历史记录 | "上次是怎么做的？" |
| **查命令** | 查询命令用法 | "git 怎么回退？" |

**执行方式：** 直接执行，无需确认

---

### 计算类（秒级任务）

| 任务 | 说明 | 示例 |
|------|------|------|
| **简单计算** | 基础数学计算 | "123 + 456 = ?" |
| **单位转换** | 单位换算 | "1MB 等于多少 KB？" |
| **时间计算** | 日期时间计算 | "3 天后是几号？" |
| **数据统计** | 简单统计 | "这组数的平均值是多少？" |

**执行方式：** 直接计算，给出结果

---

### 生成类（分钟级任务）

| 任务 | 说明 | 示例 |
|------|------|------|
| **生成代码片段** | 小段代码（< 20 行）| "写个快速排序" |
| **生成文本** | 短文本（< 200 字）| "写个请假条" |
| **生成配置** | 简单配置文件 | "写个 .gitignore" |
| **格式化** | 代码/文本格式化 | "格式化这段 JSON" |

**执行方式：** 直接生成，简单确认即可

---

### 辅助类（秒级任务）

| 任务 | 说明 | 示例 |
|------|------|------|
| **解释说明** | 解释概念/术语 | "什么是闭包？" |
| **翻译** | 文本翻译 | "把这段翻译成英文" |
| **总结** | 简单总结 | "总结一下这段话" |
| **校对** | 文本校对 | "检查这段代码的语法" |

**执行方式：** 直接处理，给出结果

---

### 娱乐类（秒级任务）

| 任务 | 说明 | 示例 |
|------|------|------|
| **说笑话** | 讲个笑话 | "说个笑话" |
| **闲聊** | 简单对话 | "你好" |
| **测试** | 功能测试 | "测试一下" |

**执行方式：** 自由发挥

---

*使用智能任务管理器自动管理快速通道*
EOF
    echo -e "${GREEN}✅ 快速通道任务列表已创建${NC}"
fi

if [ -f "$WORKSPACE_DIR/通用任务执行流程.md" ]; then
    echo -e "${YELLOW}⚠️  通用任务执行流程已存在，跳过${NC}"
else
    cat > "$WORKSPACE_DIR/通用任务执行流程.md" << 'EOF'
# 通用任务执行流程

> **适用于没有专门流程文档的任务**

---

## 第0步：确认任务

| 项目 | 内容 |
|------|------|
| 任务目标 | 具体要做什么？ |
| 截止时间 | 什么时候要？ |
| 交付形式 | 文件？消息？图片？ |

**输出格式：**
```
📋 任务确认：
- 目标：XXX
- 时间：XXX
- 交付：XXX

❓ 确认无误？继续？(y/n)
```

---

## 第1步：信息收集

### 1.1 搜索记忆
```
memory_search "相关关键词"
```

### 1.2 查找文档
- 工作区是否有相关文档？
- 是否需要网络搜索？

### 1.3 确认信息源
- 需要什么信息？
- 从哪里获取？

---

## 第2步：制定方案

### 2.1 列出执行步骤
```
📋 执行方案：

方案一：XXX
- 步骤1：XXX
- 步骤2：XXX
- ...

方案二：XXX
- 步骤1：XXX
- 步骤2：XXX
- ...

❓ 选择哪个方案？(1/2)
```

### 2.2 提供多个选项
- 至少提供2个方案
- 说明各方案优缺点
- 让用户选择

---

## 第3步：执行

### 3.1 按选定方案执行
- 逐步执行
- 每个关键节点确认

### 3.2 遇到问题立即报告
```
⚠️ 遇到问题：
- 问题：XXX
- 影响：XXX
- 建议：XXX

❓ 如何处理？
```

### 3.3 不跳步骤
- 严格按方案执行
- 不省略任何步骤

---

## 第4步：验收

### 4.1 展示结果
```
✅ 执行完成：

【结果】
- XXX
- XXX

【交付物】
- 文件：XXX
- 位置：XXX
```

### 4.2 确认满意度
```
❓ 是否满足要求？
- 满意 → 完成
- 不满意 → 返工
- 有补充 → 继续
```

### 4.3 记录经验
- 这次有什么经验教训？
- 是否需要写入记忆？
- 是否需要创建流程文档？

---

## ⚠️ 强制规则

| 规则 | 说明 |
|------|------|
| **确认再执行** | 任务不确认不开始 |
| **选项让用户选** | 方案不替用户决定 |
| **问题立即报** | 遇到问题不隐瞒 |
| **节点要确认** | 关键节点要确认 |
| **结束要验收** | 完成后要确认满意度 |

---

*使用智能任务管理器自动执行任务*
EOF
    echo -e "${GREEN}✅ 通用任务执行流程已创建${NC}"
fi

if [ -f "$WORKSPACE_DIR/流程文档模板.md" ]; then
    echo -e "${YELLOW}⚠️  流程文档模板已存在，跳过${NC}"
else
    cat > "$WORKSPACE_DIR/流程文档模板.md" << 'EOF'
# 流程文档模板

> **⚠️ 创建新的任务流程文档时，使用此模板确保格式统一**

---

## 📋 文档头部

